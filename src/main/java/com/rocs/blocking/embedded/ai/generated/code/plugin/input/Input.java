package com.rocs.blocking.embedded.ai.generated.code.plugin.input;

public class Input {
   private int lines;
   private int chars;
   private int token;
   private int ifStmt;
   private double tokenLength;
   private int method;
   private int methodLength;
   private int switchStmt;
   private int loop;

    public int getLines() {
        return lines;
    }

    public void setLines(int lines) {
        this.lines = lines;
    }

    public int getChars() {
        return chars;
    }

    public void setChars(int chars) {
        this.chars = chars;
    }

    public int getToken() {
        return token;
    }

    public void setToken(int token) {
        this.token = token;
    }

    public int getIfStmt() {
        return ifStmt;
    }

    public void setIfStmt(int ifStmt) {
        this.ifStmt = ifStmt;
    }

    public double getTokenLength() {
        return tokenLength;
    }

    public void setTokenLength(double tokenLength) {
        this.tokenLength = tokenLength;
    }

    public int getMethod() {
        return method;
    }

    public void setMethod(int method) {
        this.method = method;
    }

    public int getMethodLength() {
        return methodLength;
    }

    public void setMethodLength(int methodLength) {
        this.methodLength = methodLength;
    }

    public int getSwitchStmt() {
        return switchStmt;
    }

    public void setSwitchStmt(int switchStmt) {
        this.switchStmt = switchStmt;
    }

    public int getLoop() {
        return loop;
    }

    public void setLoop(int loop) {
        this.loop = loop;
    }
}
